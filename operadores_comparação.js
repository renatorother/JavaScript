//retornam um valor booleano

let num_1 = 5;
let num_2 = 10;
 
//igual
console.log(num_1 == num_2); //false

//diferente
console.log(num_1 != num_2); //true

//maior
console.log(num_1 > num_2); //false

//menor
console.log(num_1 < num_2); //true

//maior ou igual
console.log(num_1 >= num_2); //false

//menor ou igual
console.log(num_1 <= num_2); //true

//estritamente igual (valor e datatype)
console.log(num_1 === num_2); //false

//estritamente diferente (valor e datatype)
console.log(num_1 !== num_2); //true

