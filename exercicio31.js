class Operacoes {

    static soma(a, b){
        return a + b;
    }
    static sub(a, b){
        return a - b;
    }
    static mult(a, b){
        return a * b;
    }
    static div(a, b){
        return a / b;
    }
}

console.log(Operacoes.soma(100, 20));
console.log(Operacoes.sub(100, 20));
console.log(Operacoes.mult(100, 20));
console.log(Operacoes.div(100, 20));

