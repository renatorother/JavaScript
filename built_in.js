// Funções Matemáticas
console.log(Math.PI); // 3,1415926535897932384626433832795
console.log(Math.round(4.7)); // 5 - arredonda pra cima
console.log(Math.round(4.4)); // 4 - arredonda pra baixo
console.log(Math.pow(8,2)); // 8 elevado a 2 = 64
console.log(Math.sqrt(64)); // raíz quadrada de 64 = 8

// Funções de Data
const date = new Date();
console.log(date); // exibe a data atual
console.log(date.getFullYear()); // exibe o ano atual
console.log(date.getMonth()); // exibe o mês atual
console.log(date.getDay()); // exibe o dia atual


