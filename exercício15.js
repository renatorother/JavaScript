let number_1 = 10;
let number_2 = 20;
let operation = '/';

if ( operation == '+') {
    console.log(number_1 + number_2);
} else if ( operation == '-') {
    console.log(number_1 - number_2);
} else if ( operation == '*') {
    console.log(number_1 * number_2);
} else if ( operation == '/') {
    console.log(number_1 / number_2);
} else {
    console.log('Operação inválida');
}



